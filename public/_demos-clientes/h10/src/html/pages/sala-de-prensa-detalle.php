              <div class="titulo">Habitaciones y Suites para el máximo confort</div>
              <div class="texto">
                El ambiente de sofisticada frescura se traslada también a las amplias y luminosas habitaciones de distintas categorías del hotel: Superiores, Deluxe, Familiares, Junior Suites y la Suite Croma.
                </br>
                Todas y cada una de las estancias son exteriores y cuentan con una original terraza, obra de Rafael Moneo, diseñada como un cuadrado que se adentra al edificio y que representa una explosión de color en la blanca fachada. Las paredes y el techo están revestidos con un impactante mosaico de cerámica de Ceviran, elaborado artesanalmente en un taller de Granada y pintado a mano al estilo andaluz Mensaque. El diseño, que ha seguido un patrón único para cada terraza, está presente en azul, verde o color cobre, dando una singular coloración a la fachada.
                </br>
                En contraste con la explosión de color de las terrazas, los interiores de las habitaciones buscan la neutralidad. Predominan los tonos claros que combinan a la perfección con los colores tierra de las maderas de roble del mobiliario y los tonos arcillosos presentes en los tapizados; mientras en los pasillos encontramos una moqueta con una gráfica floral basada en interpretaciones abstractas de biznagas malagueñas y claveles. Destacan también los cabeceros de las camas, obras cubistas con líneas abstractas de colores, y los baños, todos ellos con luz natural, la cual se adentra por una ventana que, además de aportar un plus de luminosidad a la estancia, la dota de una gran personalidad, ya que desde el baño se puede disfrutar también del exterior.
                </br>
                Entre las distintas estancias se distinguen las elegantes y acogedoras Habitaciones Doble Superior Catedral, con unas privilegiadas vistas a la Catedral de Málaga, y las Junior Suites, espaciosas estancias diáfanas de 38 m2 con un dormitorio con zona de estar y una terraza amueblada con fantásticas vistas al centro de la ciudad. Mención aparte merece la exclusiva Suite Croma, una estancia de 55 m2 con dos espacios diferenciados, el dormitorio y un salón independiente con sofá-cama, así como un amplio baño y dos terrazas privadas.
