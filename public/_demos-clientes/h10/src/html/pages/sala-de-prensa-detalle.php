              <div class="texto">
                La gran seña de identidad de este proyecto de construcción ideado por el propio Rafael Moneo es un edificio que se presenta como un volumen sólido de color blanco con huecos cuadrados que se adentran en el edificio configurando las terrazas de las habitaciones, las cuales juegan con un especial cromatismo. Así, la fachada del hotel se inspira en el encalado de las casas de los pueblos blancos propios de Andalucía e incorpora terrazas adornadas con mosaicos de cerámica, un guiño a los azulejos típicos del Sur.
                </br>
                Además, todo el proyecto arquitectónico está basado en el aprovechamiento de la luz natural en todas sus estancias. Prueba de ello es que todas sus habitaciones tienen terraza con una gran cristalera e incluso los baños de las habitaciones, ubicados en el perímetro del edificio, cuentan con luz natural. Asimismo, una gran claraboya en el atrio del edificio baña de luz los pasillos de las habitaciones, un espacio que se adorna con una obra escultórica en suspensión de la artista Soledad Sevilla. Este mismo eje principal del edificio es el que también aporta luz natural al distribuidor de los 3 salones de reuniones.
                </br></br>
                El <b>interiorismo</b> del establecimiento, realizado en colaboración con el estudio de <b>Lázaro Rosa-Violán</b>, está también inspirado en el legado cultural malagueño e incorpora elementos propios del cubismo picassiano y del mediterráneo que aportan un gran dinamismo y modernidad. Esto se consigue a través de una cuidada decoración que mezcla piezas de mobiliario de diseño contemporáneo con algunas notas de carácter escandinavo y un original atrezzo a modo de bajorrelieves de pared, tapices, alfombras, cuadros y esculturas.
