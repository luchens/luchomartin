                Este sensacional Rooftop se convierte también en el escenario perfecto para la celebración de todo tipo de eventos, para aquellos que busquen ese plus añadido de ofrecer una de las mejores vistas panorámicas de la ciudad. A este exclusivo espacio se suman tres salones con luz natural y equipados con todo lo necesario para la organización de reuniones y celebraciones, entre los que destaca la Sala Cubismo, de 238 m2 y que puede acoger hasta 200 personas. Este espacio, de 5 metros de altura, cuenta con grandes ventanales con vistas a la Plaza Hoyo de Esparteros.
