              <div class="texto">
                La ciudad de Málaga cuenta con un inédito referente de diseño, el nuevo H10 Croma Málaga. El hotel cautiva a primera vista por su espectacular fachada blanca que contrasta con los juegos cromáticos de las terrazas de sus habitaciones. Esto es lo que ha inspirado el nombre del hotel, cuyo proyecto de arquitectura es obra del reconocido estudio de Rafael Moneo.
                </br></br>
                El establecimiento de 4 estrellas, que es el primero de H10 Hotels en Málaga, cuenta con una ubicación inmejorable en el centro histórico de la ciudad. Un hotel perfecto para descubrir la capital malagueña, ya que se encuentra muy próximo a los principales puntos de interés de la ciudad, como el Mercado Central de Atarazanas y la conocida calle Marqués de Larios, la zona comercial de la ciudad, con una gran oferta de restaurantes. El hotel también es un muy buen punto de partida para acceder a conocer otros espacios emblemáticos de la ciudad, como el Museo Picasso y el Museo Carmen Thyssen, el puerto de Málaga o el Teatro Romano salones de reuniones, gimnasio, un magnífico Rooftop con plunge pool y zona de hamacas con impresionantes vistas a la ciudad y un parking para huéspedes.
              </div>
            </div>

            <div class="modulo">
              <div class="titulo">Un proyecto con nombre propio</div>
              <div class="col">
                <div clasS="col-left">
                  <div class="texto">La arquitectura del nuevo H10 Croma Málaga lleva la firma del reconocido <b>estudio de Rafael Moneo</b>, Premio Pritzker de Arquitectura y Premio Príncipe de Asturias de las Artes. Además del edificio del hotel, el proyecto ha contemplado una reorganización urbanística de toda la zona donde se encuentra emplazado, junto a la Plaza Hoyo de Esparteros, donde anteriormente se ubicaba el edificio histórico de La Mundial, un icono de la ciudad que ha sido reconstruido a escasos metros respetando su tamaño y proporciones, y que conserva todas las carpinterías originales en sus puertas, ventanas y cerrajería, así como sus rejas ornamentales.</div>
                </div>
                <div class="col-right">
                  <img src="../../images/Cabecera_Malaga.jpg" class="image" alt="" />
