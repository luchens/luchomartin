              <div class="titulo">H10 Hotels abre el H10 Croma Málaga, un nuevo referente de diseño y modernidad en la ciudad</div>
              <div class="fecha">03/11/2022 - Barcelona</div>
              <div class="image-holder">
                <img src="../../images/Cabecera_Malaga.jpg" class="image" alt="" />
