                En la misma planta y con acceso desde la calle, concretamente desde la Plaza Hoyo de Esparteros, se encuentra <b>La Mundial</b>, una encantadora taberna con terraza, impregnada del espíritu malagueño y con una rompedora decoración. Destaca una imponente barra alicatada que preside el espacio y el suelo, de cerámica y de estilo andaluz, así como un llamativo cuadro protagonizado por la figura de una ‘bailaora’ y las barricas de madera dispuestas en la pared detrás de la barra, donde se disponen las botellas de vino. La Mundial, que abrirá sus puertas próximamente, es un espacio dedicado a la ciudad y a sus habitantes, el lugar ideal donde disfrutar de una selección de los mejores vinos de Málaga y de deliciosas tapas y aperitivos tradicionales malagueños, elaborados con productos locales, como el Cazón en adobo, los boquerones en sus distintas versiones o las Albondiguillas en salsa de almendras, todo ello perfecto para compartir. Tendrán un protagonismo especial también los cucuruchos de pescadito frito y de calamar frito, entre otros, muy típicos de los locales de zona.
                </br>
                El hotel cuenta también con un elegante <b>Lobby Bar</b>, ideal para relajarse y desconectar disfrutando de unos snacks, un cóctel o un café, en un agradable ambiente, donde el interiorismo juega un papel clave envolviendo al huésped en un entorno acogedor y de diseño.
                </br>
                Uno de los tesoros del H10 Croma Málaga es, sin duda, su <b>Rooftop</b>, un idílico espacio ubicado en la última planta del hotel, con una impresionante terraza con vistas 360o a toda la ciudad de Málaga, en especial de su Catedral, y que alcanzan el mar. Este espacio al aire libre dispone de una espectacular plunge pool y solárium con zona de hamacas, así como un bar de piscina con zona de mesas, donde se sirven snacks, tapas y cócteles, durante todo el día.
